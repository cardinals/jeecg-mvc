package com.emotte.eserver.core.helper;

public class ArrayHelper {
	/**
	 * 判断数组[ts]中是否包含[t]
	 * @param ts
	 * @param t
	 * @return
	 * 2014年9月2日
	 */
	public static <T> boolean contains(T[] ts, T t) {
		for (int i = 0; i < ts.length; i++) {
			if (ts[i].equals(t)) return Boolean.TRUE;
		}
		return Boolean.FALSE;
	}
}
